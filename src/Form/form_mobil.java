    public void TampilData() {
        DftTblModel_mobil = new DefaultTableModel();
        DftTblModel_mobil.addColumn("KODE MOBIL");
        DftTblModel_mobil.addColumn("NAMA MOBIL");
        DftTblModel_mobil.addColumn("JENIS MOBIL");
        DftTblModel_mobil.addColumn("JUMLAH");
        table_mobil.setModel(DftTblModel_mobil);

        Connection conn = koneksi.getConnection();
        try {
            java.sql.Statement stmt = conn.createStatement();
            SQL = "select * from mobil";
            java.sql.ResultSet res = stmt.executeQuery(SQL);
            while (res.next()) {
                DftTblModel_mobil.addRow(new Object[]{
                    res.getString("kode_mobil"),
                    res.getString("nama_mobil"),
                    res.getString("jenis_mobil"),
                    res.getString("jumlah")
                });
            }
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }

