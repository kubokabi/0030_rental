    private void button_editActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_editActionPerformed
        // TODO add your handling code here: 
        try {
            Connection conn = koneksi.getConnection();
            PreparedStatement stmt = conn.prepareStatement("update mobil set nama_mobil=?, jenis_mobil=?, jumlah=?where kode_mobil=?");
            stmt.setString(1, nama_mobil.getText());
            stmt.setString(2, jenis_mobil.getText());
            stmt.setString(3, jumlah.getText());
            stmt.setString(4, kode_mobil.getText());
            stmt.executeUpdate();
            JOptionPane.showMessageDialog(null, "Data berhasil diubah", "Pesan",
                    JOptionPane.INFORMATION_MESSAGE);
            TampilData();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_button_editActionPerformed

    private void button_simpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_simpanActionPerformed
        // TODO add your handling code here:
        String kd_mobil = kode_mobil.getText();
        String nm_mobil = nama_mobil.getText();
        String jenisMobil = jenis_mobil.getText();
        String jml = jumlah.getText();

        Connection conn = koneksi.getConnection();
        try {
            java.sql.PreparedStatement stmt = conn.prepareStatement("insert into mobil values(?,?,?,?)");
            stmt.setString(1, kd_mobil);
            stmt.setString(2, nm_mobil);
            stmt.setString(3, jenisMobil);
            stmt.setString(4, jml);
            stmt.executeUpdate();

            button_baruActionPerformed(evt);
            this.TampilData();

            javax.swing.JOptionPane.showMessageDialog(null, "Data berhasil disimpan");
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }//GEN-LAST:event_button_simpanActionPerformed

    private void table_mobilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_table_mobilMouseClicked
        int baris = table_mobil.getSelectedRow();

        kode_mobil.setText(DftTblModel_mobil.getValueAt(baris, 0).toString());
        nama_mobil.setText(DftTblModel_mobil.getValueAt(baris, 1).toString());
        jenis_mobil.setText(DftTblModel_mobil.getValueAt(baris, 2).toString());
        jumlah.setText(DftTblModel_mobil.getValueAt(baris, 3).toString());
    }//GEN-LAST:event_table_mobilMouseClicked

    private void button_hapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_button_hapusActionPerformed
        Connection conn = koneksi.getConnection();
        int confirm = JOptionPane.showConfirmDialog(null, "Apakah anda yakin ingin menghapus data tersebut?", "Konfirmasi", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (confirm == 0) {
            try {
                java.sql.PreparedStatement stmt = conn.prepareStatement("delete from mobil where kode_mobil ='" + kode_mobil.getText() + "'");
                stmt.executeUpdate();
                JOptionPane.showMessageDialog(null, "Data berhasil dihapus", "Pesan", JOptionPane.INFORMATION_MESSAGE);
                TampilData();
                kode_mobil.setText("");
                nama_mobil.setText("");
                jenis_mobil.setText("");
                jumlah.setText("");
                kode_mobil.requestFocus();
            } catch (SQLException e) {
                JOptionPane.showMessageDialog(null, "Data gagal di hapus" + e.getMessage(), "Pesan", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_button_hapusActionPerformed

